public class MathDemo{
    public static void main(String[] args){
        
        //absolute value
        
        double a = Math.abs(1);
        
        //square root something
        a = Math.sqrt(a);
        
        //use exponents
        
        a = Math.exp(1);

        //find the sin, cos, and tan of an angle

        a = Math.sin(a);
        a = Math.cos(a);
        a = Math.tan(a);
        
        //area of a circle
        
        a = Math.PI * Math.pow(a,2);
    }
}